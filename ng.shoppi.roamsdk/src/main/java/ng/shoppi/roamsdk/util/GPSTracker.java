    /**
     * Stop using GPS listener
     * Calling this method will stop using GPS in your app
     */
    public void stopUsingGPS() {
        if (locationManager != null) {
            if (checkPermission()){
                locationManager.removeUpdates(GPSTracker.this);
            }
        }
    }

    private boolean checkPermission() {
        if ( Build.VERSION.SDK_INT >= 23 &&
                ContextCompat.checkSelfPermission(mContext, Manifest.permission.ACCESS_FINE_LOCATION ) != PackageManager.PERMISSION_GRANTED &&
                ContextCompat.checkSelfPermission(mContext, Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {

            ActivityCompat.requestPermissions((AppCompatActivity)mContext,
                    new String[]{Manifest.permission.ACCESS_FINE_LOCATION},
                    1);

        }

        return true;
    }

    /**
     * Function to show settings alert dialog
     */
    public void showSettingsAlert(final Context context) {
        AlertDialog.Builder alertDialog = new AlertDialog.Builder(mContext);

        //Setting Dialog Title
        alertDialog.setTitle("Set GPS");

        //Setting Dialog Message
        alertDialog.setMessage("Your GPS is off. Please enable GPS to use the App");

        //On Pressing Setting button
        alertDialog.setPositiveButton("Go to Settings", new DialogInterface.OnClickListener() {

            @Override
            public void onClick(DialogInterface dialog, int which) {
                Intent intent = new Intent(Settings.ACTION_LOCATION_SOURCE_SETTINGS);
                ((AppCompatActivity)mContext).startActivityForResult(intent,100);
            }
        });

        //On pressing cancel button
        alertDialog.setNegativeButton("cancel", new DialogInterface.OnClickListener() {

            @Override
            public void onClick(DialogInterface dialog, int which) {
                dialog.cancel();
                ((AppCompatActivity)context).finish();
            }
        });

        alertDialog.setCancelable(false);

        alertDialog.show();
    }


